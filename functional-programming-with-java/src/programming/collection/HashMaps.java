package programming.collection;

import java.util.HashMap;

public class HashMaps {
	
	public static void main(String[] args) {
		HashMap<String, String> m = new HashMap<String, String>();
		m.put("A", "Apple");
		m.put("Z", "Zebra");
		m.put("Ajay", "Java");
		m.put("B", "Boy");
		m.put("T", "Tiger");
		System.out.println(m);
		
	}

}
